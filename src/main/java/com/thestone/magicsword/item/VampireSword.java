import net.minecraft.item.ToolMaterial;
import net.minecraft.registry.tag.DamageTypeTags;

import java.util.function.Consumer;

public class VampireSword extends SwordItem {
    public VampireSword(ToolMaterial toolMaterial, int attackDamage, float attackSpeed, Settings settings) {
        super(toolMaterial, attackDamage, attackSpeed, settings);
    }

    @Override
    public boolean postHit(ItemStack stack, LivingEntity target, LivingEntity attacker) {
        target.addStatusEffect(new StatusEffectInstance(ModEffects.PIERCE, 40,2));
        return super.postHit(stack, target, attacker);
    }
}
